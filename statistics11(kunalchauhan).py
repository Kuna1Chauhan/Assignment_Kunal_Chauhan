# d. Check equality of means
# One-way ANOVA
f_statistic, p_value = f_oneway(data['BP(Before)'], data['BP(After)'])
if p_value < 0.05:
    print("There are significant differences in means between at least one pair of classes.")
else:
    print("The means of all classes are equal.")